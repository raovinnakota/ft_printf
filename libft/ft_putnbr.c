#include "libft.h"

void	ft_putnbr(int n)
{
	int	max;

	max = 2147483647;
	if (n == -max - 1)
		ft_putstr("-2147483648");
	else
	{
		if (n < 0)
		{
			ft_putchar('-');
			n = -n;
		}
		if (n > 9)
			ft_putnbr(n / 10);
		ft_putchar('0' + (n % 10));
	}
}
